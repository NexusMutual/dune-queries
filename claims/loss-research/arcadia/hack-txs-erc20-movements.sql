  m.cover_id,
  m.sub_account,
  m.block_time,
  m.block_number,
  m.flow_type,
  m.contract_address,
  t.symbol,
  m.value / power(t.decimals, 10) as amount,
  m.evt_index,
  m.evt_tx_from,
  m.evt_tx_to,
  m."from",
  m."to",
  m.tx_hash
from asset_movements m
  left join tokens.erc20 t on m.contract_address = t.contract_address and t.blockchain = 'base'
