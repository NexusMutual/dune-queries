asset_movements as (
  select
    a.cover_id,
    a.sub_account,
    e.evt_block_time as block_time,
    e.evt_block_number as block_number,
    'out' as flow_type,
    e.contract_address,
    e.evt_tx_from,
    e.evt_tx_to,
    e."from",
    e."to",
    e.value,
    e.evt_index,
    e.evt_tx_hash as tx_hash
  from erc20_base.evt_transfer e
    inner join address_to_check a on e."from" = a.sub_account and e.evt_tx_hash = a.hack_tx_hash
  union all
  select
    a.cover_id,
    a.sub_account,
    e.evt_block_time as block_time,
    e.evt_block_number as block_number,
    'in' as flow_type,
    e.contract_address,
    e.evt_tx_from,
    e.evt_tx_to,
    e."from",
    e."to",
    e.value,
    e.evt_index,
    e.evt_tx_hash as tx_hash
  from erc20_base.evt_transfer e
    inner join address_to_check a on e.to = a.sub_account and e.evt_tx_hash = a.hack_tx_hash
)

